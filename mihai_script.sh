#!/bin/usr/bash

echo -e "\e[32m"===========================================================System Data====================================================================""
echo "HOSTNAME ...........................................................$HOSTNAME"
echo "DOMAIN NAME ........................................................$(domainname)"
echo "F.Q.D.N  ...........................................................$(hostname --fqdn)"
echo "IP ADDRESS .........................................................$(hostname -I)"
echo "OS .................................................................$(uname -o)"
echo "KERNEL VERSION .....................................................$(uname -r)"
echo "SYSTEM UPTIME ......................................................$(uptime -p)"
echo "LAST REBOOT.........................................................$(who -b | sed -e 's/^ [ \t]*//')"

echo -e "\e[33m"===========================================================Resource Data==================================================================""
echo "LOAD AVERAGES.......................................................$(uptime | cut -d ':' -f4) - (1, 5, 15)min"
echo "CPU CORES...........................................................$(nproc)"
echo "MEMORY..............................................................$(free -m | grep 1 | awk 'NR==1{print "USED " ,$3, "MB OUT OF", $2, "MB " ;exit}')"
echo "SWAP................................................................$(free -m | awk 'NR==3{print "USED " ,$3, "MB OUT OF", $2, "MB " ;exit}')"

echo -e "\e[34m"===========================================================User Data======================================================================""
echo "CURRENT USERNAME....................................................$(whoami)"
echo "LOGGED IN USERS.....................................................$(w -u | awk ' NR==1{print "THERE IS CURRENTLY",$4 ,$5, "LOGGED IN "} ' | tr ',' ' ' )"
echo "PROCESSES...........................................................$(ps -A --no-headers | wc -l ) OF $(cat /proc/sys/kernel/pid_max) MAX "
echo "=========================================================================================================================================="
